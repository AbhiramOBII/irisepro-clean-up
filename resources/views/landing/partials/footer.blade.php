    <!-- Modal and Form Handling Script -->
    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // Modal elements
            const modalOverlay = document.getElementById('common-modal-overlay');
            const modal = document.getElementById('common-modal');
            const modalCloseBtn = document.getElementById('common-modal-close');
            const modalTitle = document.getElementById('common-modal-title');
            const modalSubtitle = document.getElementById('common-modal-subtitle');
            const form = document.getElementById('common-form');
            const submitBtn = document.getElementById('common-submit-btn');
            const formMessages = document.getElementById('common-form-messages');
            const successMessage = document.getElementById('common-success-message');
            const errorMessage = document.getElementById('common-error-message');
            const successMessageText = document.getElementById('common-success-message-text');
            const errorMessageText = document.getElementById('common-error-message-text');
            const errorList = document.getElementById('common-error-list');
            const programSelect = document.getElementById('common-program');
            
            // Find all CTA buttons
            const ctaButtons = document.querySelectorAll('a[href="#"]');
            
            // Add click event to all CTA buttons
            ctaButtons.forEach(button => {
                button.addEventListener('click', function(e) {
                    e.preventDefault();
                    
                    // Set modal title and subtitle based on button text or data attributes
                    let programType = '';
                    
                    if (this.textContent.trim().toLowerCase().includes('enroll')) {
                        modalTitle.textContent = 'Enroll in iRiSEPro™';
                        modalSubtitle.textContent = 'Take the first step toward accelerating your career';
                        
                        // Try to determine which program was clicked
                        const card = this.closest('.rounded-lg');
                        if (card) {
                            const programTitle = card.querySelector('h3');
                            if (programTitle) {
                                programType = programTitle.textContent.trim();
                            }
                        }
                    } else if (this.textContent.trim().toLowerCase().includes('explore')) {
                        modalTitle.textContent = 'Explore Our Programs';
                        modalSubtitle.textContent = 'Discover how iRiSEPro can help you transform';
                    } else {
                        modalTitle.textContent = 'Begin Your Transformation';
                        modalSubtitle.textContent = 'Please fill in your details to start your journey with iRiSEPro';
                    }
                    
                    // Pre-select program in dropdown if we determined it
                    if (programType) {
                        for (let i = 0; i < programSelect.options.length; i++) {
                            if (programSelect.options[i].text.includes(programType) || 
                                programType.includes(programSelect.options[i].text)) {
                                programSelect.selectedIndex = i;
                                break;
                            }
                        }
                    }
                    
                    // Show modal
                    openModal();
                });
            });
            
            // Open modal function
            function openModal() {
                // Reset form and messages
                form.reset();
                formMessages.classList.add('hidden');
                successMessage.classList.add('hidden');
                errorMessage.classList.add('hidden');
                errorList.innerHTML = '';
                document.getElementById('common-form-success').classList.add('hidden');
                
                // Show modal with animation
                modalOverlay.classList.remove('hidden');
                modal.classList.remove('hidden');
                
                // Trigger animation after a small delay
                setTimeout(() => {
                    modalOverlay.classList.add('opacity-100');
                    modal.classList.remove('opacity-0', 'scale-95');
                    modal.classList.add('opacity-100', 'scale-100');
                }, 10);
            }
            
            // Close modal function
            function closeModal() {
                // Hide modal with animation
                modalOverlay.classList.remove('opacity-100');
                modal.classList.remove('opacity-100', 'scale-100');
                modal.classList.add('opacity-0', 'scale-95');
                
                // Complete hiding after animation
                setTimeout(() => {
                    modalOverlay.classList.add('hidden');
                    modal.classList.add('hidden');
                }, 300);
            }
            
            // Close modal when clicking close button
            modalCloseBtn.addEventListener('click', closeModal);
            
            // Close modal when clicking outside
            modalOverlay.addEventListener('click', function(e) {
                if (e.target === modalOverlay) {
                    closeModal();
                }
            });
            
            // Form submission using jQuery for better compatibility
            $(form).on('submit', function(e) {
                e.preventDefault();
                
                // Change button state
                submitBtn.disabled = true;
                $(submitBtn).html('<i class="fas fa-spinner fa-spin mr-2"></i> Submitting...');
                
                // Reset messages
                $(formMessages).addClass('hidden');
                $(successMessage).addClass('hidden');
                $(errorMessage).addClass('hidden');
                $(errorList).empty();
                
                // Use jQuery AJAX for better compatibility
                $.ajax({
                    url: form.action,
                    type: 'POST',
                    data: new FormData(form),
                    processData: false,
                    contentType: false,
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content'),
                        'X-Requested-With': 'XMLHttpRequest'
                    },
                    success: function(data) {
                        $(formMessages).removeClass('hidden');
                        
                        if (data.success) {
                            // Show success message
                            $(successMessage).removeClass('hidden');
                            $(successMessageText).text(data.message || 'Thank you for your interest! We\'ll contact you shortly.');
                            form.reset();
                            
                            // Hide form and show success message
                            $(form).addClass('hidden');
                            $('#common-form-success').removeClass('hidden');
                            
                            // Close modal after 3 seconds
                            setTimeout(function() {
                                closeModal();
                                // Reset form visibility after modal is closed
                                setTimeout(function() {
                                    $(form).removeClass('hidden');
                                    $('#common-form-success').addClass('hidden');
                                }, 500);
                            }, 3000);
                        } else {
                            // Show error message
                            $(errorMessage).removeClass('hidden');
                            $(errorMessageText).text(data.message || 'An error occurred. Please check your information and try again.');
                            
                            // Display validation errors if any
                            if (data.errors) {
                                $.each(data.errors, function(key, messages) {
                                    var li = $('<li>').text(messages[0]);
                                    $(errorList).append(li);
                                });
                            }
                        }
                    },
                    error: function(xhr, status, error) {
                        console.error('AJAX Error:', status, error);
                        $(formMessages).removeClass('hidden');
                        $(errorMessage).removeClass('hidden');
                        
                        // Try to parse response if possible
                        try {
                            var response = JSON.parse(xhr.responseText);
                            $(errorMessageText).text(response.message || 'Server error occurred. Please try again.');
                        } catch (e) {
                            $(errorMessageText).text('An error occurred: ' + error);
                        }
                    },
                    complete: function() {
                        // Reset button state
                        submitBtn.disabled = false;
                        $(submitBtn).html('<i class="fas fa-paper-plane mr-2"></i> Submit');
                    }
                });
            });
        });
    </script>
